CREATE SCHEMA IF NOT EXISTS INF_FILE;

CREATE TABLE IF NOT EXISTS INF_FILE.FILE_RESOURCE (
    ID BIGSERIAL PRIMARY KEY,
    RESOURCE_TYPE VARCHAR(20) NOT NULL,
    NAME VARCHAR(255) NOT NULL,
    URL TEXT,
    BUCKET_NAME VARCHAR(255) NOT NULL,
    OBJECT_PATH TEXT NOT NULL,
    PARENT_PATH TEXT,
    PATH_DEPTH INTEGER NOT NULL DEFAULT 0,
    PATH_1 VARCHAR(255),
    PATH_2 VARCHAR(255),
    PATH_3 VARCHAR(255),
    PATH_4 VARCHAR(255),
    PATH_5 VARCHAR(255),
    PATH_6 VARCHAR(255),
    PATH_7 VARCHAR(255),
    PATH_8 VARCHAR(255),
    PATH_9 VARCHAR(255),
    PATH_10 VARCHAR(255),
    EXTENSION VARCHAR(50),
    CONTENT_TYPE VARCHAR(255),
    FILE_SIZE BIGINT,
    ETAG VARCHAR(255),
    VERSION_ID VARCHAR(255),
    STATUS VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    CREATED_BY VARCHAR(100),
    UPDATED_BY VARCHAR(100),
    CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT UK_FILE_RESOURCE_OBJECT_PATH UNIQUE (OBJECT_PATH),
    CONSTRAINT CK_FILE_RESOURCE_TYPE CHECK (RESOURCE_TYPE IN ('FILE', 'FOLDER', 'ZIP')),
    CONSTRAINT CK_FILE_RESOURCE_STATUS CHECK (STATUS IN ('ACTIVE', 'DELETED'))
);

