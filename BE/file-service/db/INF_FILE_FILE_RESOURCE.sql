-- 1. Get URL theo name
-- Neu co trung ten, can bo sung them PATH_1, PATH_2... de dinh danh dung.
SELECT
    ID,
    NAME,
    URL,
    OBJECT_PATH,
    RESOURCE_TYPE,
    STATUS
FROM INF_FILE.FILE_RESOURCE
WHERE STATUS = 'ACTIVE'
  AND NAME = '20260426_013347_8cae9c69.jpg';

-- 1b. Get URL theo name + filter cap 1, cap 2
SELECT
    ID,
    NAME,
    URL,
    OBJECT_PATH
FROM INF_FILE.FILE_RESOURCE
WHERE STATUS = 'ACTIVE'
  AND NAME = '20260426_013347_8cae9c69.jpg'
  AND PATH_1 = 'infinite-world'
  AND PATH_2 = 'avatar';

-- 2. Lay danh sach truc tiep ben trong folder
SELECT
    ID,
    NAME,
    RESOURCE_TYPE,
    OBJECT_PATH,
    URL,
    FILE_SIZE,
    CONTENT_TYPE
FROM INF_FILE.FILE_RESOURCE
WHERE STATUS = 'ACTIVE'
  AND PARENT_PATH = 'infinite-world/avatar'
ORDER BY
    CASE WHEN RESOURCE_TYPE = 'FOLDER' THEN 0 ELSE 1 END,
    NAME;

-- 3. Search theo cap 1
SELECT *
FROM INF_FILE.FILE_RESOURCE
WHERE STATUS = 'ACTIVE'
  AND PATH_1 = 'infinite-world'
ORDER BY OBJECT_PATH;

-- 4. Search theo cap 1 + cap 2
SELECT *
FROM INF_FILE.FILE_RESOURCE
WHERE STATUS = 'ACTIVE'
  AND PATH_1 = 'infinite-world'
  AND PATH_2 = 'avatar'
ORDER BY OBJECT_PATH;

-- 5. Search theo cap 1 + cap 2 + cap 3
SELECT *
FROM INF_FILE.FILE_RESOURCE
WHERE STATUS = 'ACTIVE'
  AND PATH_1 = 'infinite-world'
  AND PATH_2 = 'project'
  AND PATH_3 = 'a'
ORDER BY OBJECT_PATH;

-- 6. Search tong hop, co the dung cho native query voi tham so nullable
SELECT *
FROM INF_FILE.FILE_RESOURCE
WHERE STATUS = 'ACTIVE'
  AND (:path1 IS NULL OR PATH_1 = :path1)
  AND (:path2 IS NULL OR PATH_2 = :path2)
  AND (:path3 IS NULL OR PATH_3 = :path3)
  AND (:path4 IS NULL OR PATH_4 = :path4)
  AND (:path5 IS NULL OR PATH_5 = :path5)
  AND (:name IS NULL OR NAME = :name)
  AND (:resourceType IS NULL OR RESOURCE_TYPE = :resourceType)
ORDER BY OBJECT_PATH;

-- 7. Soft delete khi object da xoa khoi MinIO
UPDATE INF_FILE.FILE_RESOURCE
SET STATUS = 'DELETED',
    UPDATED_AT = CURRENT_TIMESTAMP,
    UPDATED_BY = 'system'
WHERE OBJECT_PATH = 'infinite-world/avatar/20260426_013347_8cae9c69.jpg';
